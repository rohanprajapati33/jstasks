//area of square
function areasquare(side) {
  let area = side * side;
  return area;
}

console.log("Area = " + areasquare(4));

//area of rectangle
function areaRectangle(a, b) {
  let area = a * b;
  return area;
}

console.log("Area = " + areaRectangle(5, 6));

//area of tringle
function areatringle(k, l, m) {
  let area = k * l * m;
  return area;
}

console.log("Area =" + areatringle(15, 20, 25));
